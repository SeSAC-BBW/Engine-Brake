/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC_Init();
  MX_USART4_UART_Init();
  /* USER CODE BEGIN 2 */
	
	HAL_Delay(100);

	// LCD object create & initializing (RS: PA9, EN: PA8)
	lcd = Lcd_create(ports, pins, GPIOA, GPIO_PIN_9, GPIOC, GPIO_PIN_7, LCD_4_BIT_MODE);
	Lcd_init(&lcd);
	Lcd_string(&lcd, "Engine: OFF     ");
	
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		HAL_ADC_Start(&hadc);
		
    if (HAL_ADC_PollForConversion(&hadc, 10) == HAL_OK) {
        btn_value = HAL_ADC_GetValue(&hadc);
    }
		
		if (HAL_ADC_PollForConversion(&hadc, 10) == HAL_OK) {
        brake_value = HAL_ADC_GetValue(&hadc);
    }
    HAL_ADC_Stop(&hadc);
		
		float Brake_limit = 2500.0f;
		uint32_t mapped_brake = 0; // Sensitive brake logic
		mapped_brake = (uint32_t)(((float)brake_value / Brake_limit) * 4095.0f);
		
		if(mapped_brake > ADC_MAX)
			mapped_brake = ADC_MAX;
		
		uint8_t current_button = get_button(btn_value);
		
		if (current_button == 1 && last_button != 1) { // If press left button
			HAL_Delay(50); 
			
			if(engine_status == 0) {
				if(mapped_brake > BRAKE_THRESHOLD) {
					engine_status = 1;
					Lcd_cursor(&lcd, 0, 0);
					Lcd_string(&lcd, "Engine: ON      ");
				}
				else {
					Lcd_cursor(&lcd, 0, 0);
					Lcd_string(&lcd, "Step on Brake!  ");
					HAL_Delay(1000);
					Lcd_cursor(&lcd, 0, 0);
					Lcd_string(&lcd, "Engine: OFF      ");
				}
			}
			else {
				engine_status = 0;
				Lcd_cursor(&lcd, 0, 0);
				Lcd_string(&lcd, "Engine: OFF      ");
			}
			HAL_UART_Transmit(&huart4, &engine_status, 1, 10);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		}
		char debug_buf[20];
    sprintf(debug_buf, "Brake:%4lu", (unsigned long)mapped_brake);
    Lcd_cursor(&lcd, 1, 0);
    Lcd_string(&lcd, debug_buf);

		static uint32_t last_send_time = 0;
    if (HAL_GetTick() - last_send_time >= 100) {
			uint8_t data_to_send;
			
			if(mapped_brake >= 4000) { // Servo trigger code
				data_to_send = 2;
			}
			else {										// Engine status
				data_to_send = engine_status;
			}
			
			// Retransmit data to Arduino by 0.1sec 
      HAL_UART_Transmit(&huart4, &data_to_send, 1, 10);
      last_send_time = HAL_GetTick();
    }

    last_button = current_button;
    HAL_Delay(50);
	}
  /* USER CODE END 3 */
}

