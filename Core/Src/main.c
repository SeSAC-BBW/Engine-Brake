/* USER CODE BEGIN 0 */
uint8_t get_button(uint32_t adc_val) {
    if (adc_val < 200)  return 4;  // Right
    if (adc_val < 1300)  return 2;  // Up
    if (adc_val < 2500) return 3;  // Down
    if (adc_val < 3700) return 1;  // Left
    return 0; // None
}


